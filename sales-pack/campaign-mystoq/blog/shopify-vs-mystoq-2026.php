  <h2 id="support">5. الدعم الفني — الواقع</h2>

  <p>دعم Shopify بالإنجليزية فقط (والفرنسية لباقة Advanced). أوقات الانتظار: 30-90 دقيقة في الدردشة الحية. يجيبك "Customer Success Manager" من الفلبين لا يعرف ما هو Yalidine ولا لماذا تحتاج CCP.</p>

  <p>دعم MyStoq: ردود WhatsApp مباشرة من المؤسس (أنا) خلال ساعات العمل، بالعربية الجزائرية. متى احتجت مكالمة فيديو، نجدول واحدة عبر LIQAA Meet. صفر تذاكر، صفر انتظار، صفر "Have you tried turning it off and on again".</p>

  <blockquote>«حين كانت لدي مشكلة في Shopify، كنت أحاول شرح ما هي Edahabia لمدة 15 دقيقة قبل أن نتحدث عن المشكلة. مع MyStoq، أقول 'CCP لم يصل' ويعرف يعقوب فورا ما المقصود.» — تاجر مستحضرات تجميل من سطيف</blockquote>

  <h2 id="integration">6. التكامل مع WhatsApp Business</h2>

  <p>70٪ من الطلبيات في الجزائر تبدأ على WhatsApp. هذه ليست رفاهية، هذه ضرورة.</p>

  <p>Shopify يحتاج 3 apps تابعة (مدفوعة) لتفعل ما يفعله MyStoq أصلا:</p>
  <ol>
    <li>إرسال إشعار الطلبية للزبون عبر WhatsApp بدلا من الإيميل</li>
    <li>سؤال الزبون عن العنوان مباشرة في WhatsApp إن لم يكتبه في الشيك أوت</li>
    <li>إعلام التاجر بالطلبيات الجديدة على WhatsApp Business</li>
  </ol>

